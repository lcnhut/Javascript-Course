const blackjack = (array) => {
  let currentPoint = 0;

  return (a, b) => {
    currentPoint = a + b;
    let isBust = false;
    return () => {
      let lastPoint = currentPoint;
      if (!isBust) {
        if (currentPoint <= 21) {
          currentPoint += array.shift();
          return lastPoint;
        } else if (currentPoint > 21) {
          isBust = true;
          return "Bust";
        }
      } else if (currentPoint > 21) {
        return "You are done!";
      }
    };
  };
};

const deal = blackjack([
  2, 6, 1, 7, 11, 4, 6, 3, 9, 8, 9, 3, 10, 4, 5, 3, 7, 4, 9, 6, 10, 11,
]);

